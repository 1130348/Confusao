    private void chkHeaderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkHeaderActionPerformed
        if ((!chkIncludeHeader.isSelected() || chkIncludeHeader.isSelected())  && checkChkHeader == 0) {
            chkIncludeHeader.setEnabled(true);
            checkChkHeader = 1;
            return;
        } else {
            chkIncludeHeader.setSelected(false);
            chkIncludeHeader.setEnabled(false);
            checkChkHeader = 0;
        }
    }//GEN-LAST:event_chkHeaderActionPerformed


