    private void btnImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportActionPerformed
        try {
            int header = 0;
            int includeHeader = 0;
            if (!txtSeparator.getText().isEmpty()) {
                if (fileChoosed != null) {
                    if (chkHeader.isSelected()) {
                        header = 1;
                    }
                    if (chkIncludeHeader.isSelected()) {
                        includeHeader = 1;
                    }
                    ieController.importFiles(app, fileChoosed.getSelectedFiles(), txtSeparator.getText(), header, includeHeader);
                    dispose();
                }else {
                    JOptionPane.showMessageDialog(null, "Choose a file to be imported", "Warning", JOptionPane.WARNING_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Choose the column's separator", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ImportDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnImportActionPerformed

